public class Person {
    private String name;
    private int age;

    //setter name
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    //setter age
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}
